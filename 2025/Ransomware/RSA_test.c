void Encrypt()
{
	PBYTE KEYIV = NULL;
	PBYTE EncryptedKEYIV = NULL;
	//PBYTE AESKey = NULL;
	//PBYTE IV = NULL;

	DWORD EncryptedKEYIVSize = 0;
	DWORD bufferSize = 0;
	ULONG AESKey_Size = 32;
	ULONG IV_Size = 16;
	ULONG KEYIV_Size = 48;

	NTSTATUS status = 0;

	// AES에 쓰인 KEY와 IV를 동적할당
	// 여기서는 테스트 용도로 아무거나 설정해서 테스트
	BYTE AESKey[32] = {
		0x2c, 0xc6, 0x44, 0x7a, 0x95, 0xa7, 0xcb, 0x47,
		0x51, 0xe9, 0x75, 0x35, 0xd4, 0x1c, 0x82, 0xca,
		0x75, 0x0a, 0xeb, 0x8d, 0x4a, 0x26, 0x9e, 0xa8,
		0xe2, 0x80, 0xc8, 0x2b, 0x7e, 0xf2, 0x3f, 0x93
	};
	BYTE IV[16] = {
		0xa6, 0x97, 0x7a, 0xc5, 0x0b, 0x65, 0x1e, 0xd3,
		0xfc, 0x16, 0xef, 0x16, 0xd5, 0xb9, 0x0e, 0xae
	};

	// KEY와 IV를 KEYvalue 변수에 저장, KEYIV = RSA의 PLAIN
	KEYIV = (PBYTE)calloc(KEYIV_Size, sizeof(BYTE));
	if (KEYIV == NULL) {
		printf("Memory Allocation Fail\n");
		return;
	}
	memcpy(&KEYIV[0], AESKey, AESKey_Size);
	memcpy(&KEYIV[32], IV, IV_Size);

	for (int i = 0; i < 48; i++) {
		printf("0x%02x, ", KEYIV[i]);
	}
	////////////////////////////////////////

	// CNG setting
	BCRYPT_ALG_HANDLE RSA_Handle = BCRYPT_RSA_ALG_HANDLE;
	BCRYPT_KEY_HANDLE RSA_PublicKeyHandle = NULL;

	// Set Public Key BLOB
	PublicKey_BLOB* PublicKey = NULL;
	PublicKey = (PublicKey_BLOB*)calloc(1, sizeof(PublicKey_BLOB));
	if (PublicKey == NULL) { printf("Memory Allocation Error\n"); return; }

	PublicKey->Magic = BCRYPT_RSAPUBLIC_MAGIC;
	PublicKey->BitLength = 4096;
	PublicKey->cbPublicExp = 3;
	PublicKey->cbModulus = 512;
	PublicKey->cbPrime1 = 0;
	PublicKey->cbPrime2 = 0;
	memcpy(PublicKey->PublicExponent, e, 3);
	memcpy(PublicKey->Modulus, n, 512);

	// Import Key Pair
	status = BCryptImportKeyPair(
		RSA_Handle,
		NULL,
		BCRYPT_RSAPUBLIC_BLOB,
		&RSA_PublicKeyHandle,
		(PBYTE)PublicKey,
		RSA_PublicKey_BLOB_Size,
		BCRYPT_NO_KEY_VALIDATION);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Import Key Pair (Error code: %x)\n", status);
		return;
	}

	// Calculate EncryptedKEYIV Length
	status = BCryptEncrypt(
		RSA_PublicKeyHandle,
		KEYIV,
		48,
		NULL,
		NULL,
		0,
		NULL,
		0,
		&EncryptedKEYIVSize,
		BCRYPT_PAD_PKCS1);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Calculate KeyValue Length (Error code: %x)\n", status);
		return;
	}

	// Encrypt RSA KEYIV
	EncryptedKEYIV = (PBYTE)calloc(EncryptedKEYIVSize, sizeof(BYTE));
	if (EncryptedKEYIV == NULL) return;
	status = BCryptEncrypt(
		RSA_PublicKeyHandle,
		KEYIV,
		48,
		NULL,
		NULL,
		0,
		EncryptedKEYIV,
		EncryptedKEYIVSize,
		&bufferSize,
		BCRYPT_PAD_PKCS1);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Encrypt RSA KeyValue (Error code: %x)\n", status);
		return;
	}

	// Print Ciphertext (for testing)
	EncryptedKEYIVSize = bufferSize;
	printf("Encrypted AESKey and IV\n");
	for (int cnt_i = 0; cnt_i < EncryptedKEYIVSize; cnt_i++) {
		if (cnt_i % 16 == 0) printf("\n");
		if (cnt_i == EncryptedKEYIVSize - 1) {
			printf("0x%02x", EncryptedKEYIV[cnt_i]);
			break;
		}
		printf("0x%02x, ", EncryptedKEYIV[cnt_i]);
	}
	printf("\nEncrypted AESKey and IV Size: %d Bytes\n", EncryptedKEYIVSize);

	// Destroy Key and Close Algorithm Provider
	BCryptDestroyKey(RSA_PublicKeyHandle);
	BCryptCloseAlgorithmProvider(RSA_Handle, 0);

	// Memory Free
	free(EncryptedKEYIV);
	free(PublicKey);
	free(KEYIV);
	free(IV);
	free(AESKey);
}

