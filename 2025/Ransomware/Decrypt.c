// The lower 512 bytes of the encrypted file: AES KEY and IV concatenated and encrypted with RSA4096
// The remaining bytes: plaintext encrypted with AES256/CBC/PKCS7Padding

// Decrypt file one by one
void Decrypt(char* filePath)
{
	printf("Filename: %s\n", filePath);

	int RSAbyteNum = 0;
	int AESbyteNum = 0;

	NTSTATUS status = 0;

	// Decrypt the lower 512 bytes of the .cry file with RSA4096
	// The upper 32 bytes of the decrypted data: Key of AES256/CBC/PKCS7Padding, the lower 16 bytes: IV
	FILE* fp = fopen(filePath, "rb");
	if (fp == NULL) {
		printf("Failed to Open Encrypted File\n");
		return;
	}

	fseek(fp, 0, SEEK_END);
	ULONG fileSize = ftell(fp);
	fseek(fp, -512, SEEK_END);

	// Buffer that store lower 512 bytes
	// Hexadecimal values ​​can contain negative numbers
	unsigned char RSACipher[512]; 
	RSAbyteNum = fread(RSACipher, 1, 512, fp);
	if (RSAbyteNum != 512) {
		printf("Failed to Read Encrypted File (RSA encrypted AESkey)\n");
		return;
	}
	
	// AESCipher: Buffer that store higher bytes
	// CipherSize: fileSize - 512 byte(4096 bit)
	ULONG CipherSize = fileSize - 512;
	PBYTE AESCipher = (PBYTE)calloc(CipherSize, sizeof(BYTE));
	fseek(fp, 0, SEEK_SET);
	AESbyteNum = fread(AESCipher, 1, CipherSize, fp);
	if (AESbyteNum < 0) { // AESbyteNum == CipherSize
		printf("Failed to Read Encrypted File (AES encrypted Plain)\n");
		return;
	}
	
	// Decrypt KEYIV with RSA 4096
	BYTE* KEYIV = NULL;
	DWORD KEYIVLength = 0;
	ULONG bufferSize = 0;

	BCRYPT_ALG_HANDLE algHandle = BCRYPT_RSA_ALG_HANDLE;
	BCRYPT_KEY_HANDLE PrivateKey_Handle = NULL;
	PrivateKey_BLOB* PrivateKey = NULL;
	PrivateKey = (PrivateKey_BLOB*)calloc(1, sizeof(PrivateKey_BLOB));
	if (PrivateKey == NULL) { printf("Memory Allocation Error\n"); return; }

	PrivateKey->Magic = BCRYPT_RSAPRIVATE_MAGIC;
	PrivateKey->BitLength = 4096;
	PrivateKey->cbPublicExp = 3;
	PrivateKey->cbModulus = 512;
	PrivateKey->cbPrime1 = 256;
	PrivateKey->cbPrime2 = 256;
	memcpy(PrivateKey->PublicExponent, e, 3);
	memcpy(PrivateKey->Modulus, n, 512);
	memcpy(PrivateKey->p, p, 256);
	memcpy(PrivateKey->q, q, 256);

	status = BCryptImportKeyPair(
		algHandle,
		NULL,
		BCRYPT_RSAPRIVATE_BLOB,
		&PrivateKey_Handle,
		(PBYTE)PrivateKey,
		RSA_PrivateKey_BLOB_Size,
		BCRYPT_NO_KEY_VALIDATION);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Import Key Pair (Error code: %x)\n", status);
		return;
	}

	status = BCryptDecrypt(
		PrivateKey_Handle,
		RSACipher,
		512,
		NULL,
		NULL,
		0,
		NULL,
		0,
		&KEYIVLength,
		BCRYPT_PAD_PKCS1);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Calculate KEYIV Length (Error code: %x)\n", status);
		return;
	}

	KEYIV = (PBYTE)calloc(KEYIVLength, sizeof(BYTE));
	if (KEYIV == NULL) return;
	status = BCryptDecrypt(
		PrivateKey_Handle,
		RSACipher,
		512,
		NULL,
		NULL,
		0,
		KEYIV,
		KEYIVLength,
		&bufferSize,
		BCRYPT_PAD_PKCS1);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Decrypt KEYIV (Error code: %x)\n", status); 
		return;
	}
	BCryptDestroyKey(PrivateKey_Handle);
	BCryptCloseAlgorithmProvider(algHandle, 0);

	// Set AES256/CBC/PKCS7Padding KEY and IV
	BYTE KEY[32] = { 0x00, };
	BYTE IV[16] = { 0x00, };
	for (int i = 0; i < 32; i++) {
		KEY[i] = KEYIV[i];
	}
	for (int i = 0; i < 16; i++) {
		IV[i] = KEYIV[32 + i];
	}

	// Decrypt AESCipher with AES256/CBC/PKCS7Padding
	BYTE* Plain = NULL;
	DWORD IVLength = 16;
	DWORD BlockLength = 16;
	ULONG PlainLength = 0;

	algHandle = BCRYPT_AES_CBC_ALG_HANDLE;
	BCRYPT_KEY_HANDLE KEY_HANDLE = NULL;

	status = BCryptGenerateSymmetricKey(
		algHandle,
		&KEY_HANDLE,
		NULL,
		0,
		KEY,
		32,
		0);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Generate AES key Object (Error code: %x)\n", status);
		return;
	}

	status = BCryptDecrypt(
		KEY_HANDLE,
		AESCipher,
		AESbyteNum,
		NULL,
		IV,
		IVLength,
		NULL,
		0,
		&PlainLength,
		BCRYPT_BLOCK_PADDING);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Calculate Plain Length (Error code: %x)\n", status);
		return;
	}

	Plain = (PBYTE)calloc(PlainLength, sizeof(BYTE));
	if (Plain == NULL) return;
	status = BCryptDecrypt(
		KEY_HANDLE,
		AESCipher,
		AESbyteNum,
		NULL,
		IV,
		IVLength,
		Plain,
		PlainLength,
		&bufferSize,
		BCRYPT_BLOCK_PADDING);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Decrypt File (Error code: %x)\n", status);
		return;
	}
	BCryptDestroyKey(KEY_HANDLE);
	BCryptCloseAlgorithmProvider(algHandle, 0);
	
	// Print plaintext (For debug)
	printf("Plain: %s\n", Plain);
	
	// Create file of decrypted file
	char newFilePath[400];
	strcpy(newFilePath, filePath);
	char* ext = strrchr(newFilePath, '.');
	if (ext != NULL) *ext = '\0';
	strcat(newFilePath, "");

	//////////////////////////////////////////////////////////
	// 
	// File Extension: After decryption, Check file signature,
	// and change extension (How: Use Hex Viewer)
	// 
	//////////////////////////////////////////////////////////

	printf("Decrypted Filename: %s\n", newFilePath);

	// Open decrypted file and write plaintext
	FILE* Fp = fopen(newFilePath, "wb");
	if (Fp == NULL) {
		printf("Failed to Generate Decrypted File\n");
	}
	else {
		fwrite(Plain, sizeof(BYTE), PlainLength, Fp);
		int result = fclose(Fp);
		if (result != 0) {
			printf("Failed to Close Decrypted File\n");
			return;
		}
		else {
			printf("File was Decrypted!\n");
		}
	}

	// Memory free
	free(Plain);
	free(KEYIV);
	free(PrivateKey);
	free(AESCipher);

	// Close Encrypted File
	int Result = fclose(fp);
	if (Result != 0) {
		printf("Failed to Close Encrypted File\n");
		return;
	}
}

// Find paths of encrypted files 
void Search(const char* directory)
{
	WIN32_FIND_DATAA findData;
	HANDLE hFind;

	char filePath[MAX_PATH];
	char fileName[MAX_PATH];

	char* ext;
	sprintf(filePath, "%s\\*", directory);

	// Find .cry extension files
	hFind = FindFirstFileA(filePath, &findData);
	if (hFind == INVALID_HANDLE_VALUE) {
		printf("Failed to Search file!\n");
		return;
	}
	else if (hFind != INVALID_HANDLE_VALUE) {
		do {
			strcpy(fileName, findData.cFileName);
			ext = strrchr(fileName, '.');
			if (ext != NULL && strcmp(ext, ".cry") == 0) {
				sprintf(filePath, "%s\\%s", directory, fileName);
				Decrypt(filePath);
			}
		} while (FindNextFileA(hFind, &findData));

		FindClose(hFind);
	}
}

